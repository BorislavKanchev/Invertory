    public void IdFill() {
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InvertoryDB", "kangaro", "1234");
            St = Con.createStatement();
            Rs = St.executeQuery("SELECT MAX(Provider_id) as Provider_id FROM invertorydb.`Provider`;");
            if (Rs.next()){
            int i = Rs.getInt("Provider_id");
            int id = i+1;
            ProviderIdTextBox.setText(String.valueOf(id));
            }else{
            ProviderIdTextBox.setText("1");
            }
            Con.close();
            ProviderNameTextBox.requestFocusInWindow();
        } catch (SQLException e) {
            e.printStackTrace();
        }
     }
     
