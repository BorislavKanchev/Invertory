    private void ProviderAddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProviderAddButtonMouseClicked
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InvertoryDB", "kangaro", "1234");
            PreparedStatement add = Con.prepareStatement("insert into Provider VALUES(?,?,?)");
            add.setInt(1, Integer.valueOf(ProviderIdTextBox.getText()));
            add.setString(2, (ProviderNameTextBox.getText()));
            add.setString(3, (ProviderPhoneTextBox.getText()));

            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Доставчика е добавен");
            Con.close();
            ProviderTableFill();
            Clear();
            IdFill();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_ProviderAddButtonMouseClicked

