    private void ProviderDeleteButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProviderDeleteButtonMouseClicked
        if (ProviderIdTextBox.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Няма избран доставчик за изтриване");
        } else {
            try {
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InvertoryDB", "kangaro", "1234");
                String Id = ProviderIdTextBox.getText();
                String Qwery = "Delete from invertorydb.Provider where Provider_id=" + Id;
                Statement Add = Con.createStatement();
                Add.executeUpdate(Qwery);
                Con.close();
                ProviderTableFill();
                Clear();
                IdFill();
                JOptionPane.showMessageDialog(this, "Доставчика е изтрит");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_ProviderDeleteButtonMouseClicked

    private void ProviderUpdateButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProviderUpdateButtonMouseClicked
        if (ProviderIdTextBox.getText().isEmpty()||ProviderNameTextBox.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Липсва пълна информация за доставчик");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InvertoryDB", "kangaro", "1234");
                String UpdateQwery = "Update invertorydb.Provider set Provider_name='"+ProviderNameTextBox.getText()+"'"+",Provider_phone='"+ProviderPhoneTextBox.getText()+"'"+"where Provider_id="+ProviderIdTextBox.getText();
                Statement Add = Con.createStatement();
                Add.executeUpdate(UpdateQwery);
                Con.close();
                ProviderTableFill();
                Clear();
                IdFill();
                JOptionPane.showMessageDialog(this, "Доставчика е променен");
            }catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_ProviderUpdateButtonMouseClicked

