    private void EnterButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EnterButtonMouseClicked
       if (UserTextBox.getText().isEmpty()||PasswordField.getText().isEmpty())
       {
          JOptionPane.showMessageDialog(this, "Моля попълнете полетата"); 
       }else
       {
        String Query="Select * from User where User_name='"+UserTextBox.getText()+"' AND User_pass='"+PasswordField.getText()+"'";
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InvertoryDB", "kangaro", "1234");
            St = Con.createStatement();
            Rs = St.executeQuery(Query);
            if (Rs.next())
            {
            int i = Rs.getInt("User_role_id");
            
            if (i==1)
            {
                role="admin";
            }else
            {
                role="operator";
            }
            new HomeForm().setVisible(true);
            this.dispose();
            }else
            {
                JOptionPane.showMessageDialog(this, "Грешно потребителско име или парола");
            }
            
            Con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
       }
    }//GEN-LAST:event_EnterButtonMouseClicked

